<script>
import { ref, reactive } from 'vue'
import { useRouter } from 'vue-router'
import { useI18n } from 'vue-i18n'

export default {
  name: 'RestaurantRegister',
  setup() {
    const router = useRouter()
    const { t: $t } = useI18n()
    const loading = ref(false)
    const errorMessage = ref('')
    const successMessage = ref('')

    const form = reactive({
      // Informations du restaurant
      restaurant_name: '',
      cuisine_type: '',
      price_range: '',
      description: '',
      
      // Adresse et contact
      address: '',
      phone: '',
      email: '',
      website: '',
      
      // Horaires et capacit√©
      opening_hours: '',
      capacity: '',
      
      // √âquipements
      has_parking: false,
      has_wifi: false,
      has_outdoor_seating: false,
      is_wheelchair_accessible: false,
      
      // Propri√©taire
      owner_first_name: '',
      owner_last_name: '',
      password: '',
      confirm_password: ''
    })

    const errors = reactive({
      restaurant_name: '',
      cuisine_type: '',
      price_range: '',
      address: '',
      phone: '',
      email: '',
      opening_hours: '',
      capacity: '',
      owner_first_name: '',
      owner_last_name: '',
      password: '',
      confirm_password: ''
    })

    const validateForm = () => {
      // R√©initialiser les erreurs
      Object.keys(errors).forEach(key => {
        errors[key] = ''
      })

      let isValid = true

      // Validation nom du restaurant
      if (!form.restaurant_name.trim()) {
        errors.restaurant_name = 'Le nom du restaurant est requis'
        isValid = false
      } else if (form.restaurant_name.length < 2) {
        errors.restaurant_name = 'Le nom doit contenir au moins 2 caract√®res'
        isValid = false
      }

      // Validation type de cuisine
      if (!form.cuisine_type) {
        errors.cuisine_type = 'Le type de cuisine est requis'
        isValid = false
      }

      // Validation gamme de prix
      if (!form.price_range) {
        errors.price_range = 'La gamme de prix est requise'
        isValid = false
      }

      // Validation adresse
      if (!form.address.trim()) {
        errors.address = 'L\'adresse est requise'
        isValid = false
      }

      // Validation t√©l√©phone
      const phoneRegex = /^[0-9]{10}$/
      if (!form.phone) {
        errors.phone = 'Le t√©l√©phone est requis'
        isValid = false
      } else if (!phoneRegex.test(form.phone.replace(/\s/g, ''))) {
        errors.phone = 'Format de t√©l√©phone invalide (10 chiffres)'
        isValid = false
      }

      // Validation email
      const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/
      if (!form.email) {
        errors.email = 'L\'email est requis'
        isValid = false
      } else if (!emailRegex.test(form.email)) {
        errors.email = 'Format d\'email invalide'
        isValid = false
      }

      // Validation horaires
      if (!form.opening_hours.trim()) {
        errors.opening_hours = 'Les horaires d\'ouverture sont requis'
        isValid = false
      }

      // Validation capacit√©
      if (!form.capacity) {
        errors.capacity = 'La capacit√© est requise'
        isValid = false
      } else if (parseInt(form.capacity) < 1) {
        errors.capacity = 'La capacit√© doit √™tre sup√©rieure √† 0'
        isValid = false
      }

      // Validation propri√©taire
      if (!form.owner_first_name.trim()) {
        errors.owner_first_name = 'Le pr√©nom du propri√©taire est requis'
        isValid = false
      } else if (form.owner_first_name.length < 2) {
        errors.owner_first_name = 'Le pr√©nom doit contenir au moins 2 caract√®res'
        isValid = false
      }

      if (!form.owner_last_name.trim()) {
        errors.owner_last_name = 'Le nom du propri√©taire est requis'
        isValid = false
      } else if (form.owner_last_name.length < 2) {
        errors.owner_last_name = 'Le nom doit contenir au moins 2 caract√®res'
        isValid = false
      }

      // Validation mot de passe
      if (!form.password) {
        errors.password = 'Le mot de passe est requis'
        isValid = false
      } else if (form.password.length < 6) {
        errors.password = 'Le mot de passe doit contenir au moins 6 caract√®res'
        isValid = false
      }

      // Validation confirmation mot de passe
      if (!form.confirm_password) {
        errors.confirm_password = 'La confirmation du mot de passe est requise'
        isValid = false
      } else if (form.password !== form.confirm_password) {
        errors.confirm_password = 'Les mots de passe ne correspondent pas'
        isValid = false
      }

      return isValid
    }

    const handleRegister = async () => {
      if (!validateForm()) return

      loading.value = true
      errorMessage.value = ''
      successMessage.value = ''

      try {
        const restaurantData = {
          restaurant_name: form.restaurant_name.trim(),
          cuisine_type: form.cuisine_type,
          price_range: form.price_range,
          description: form.description.trim(),
          address: form.address.trim(),
          phone: form.phone.replace(/\s/g, ''),
          email: form.email.toLowerCase(),
          website: form.website.trim(),
          opening_hours: form.opening_hours.trim(),
          capacity: parseInt(form.capacity),
          has_parking: form.has_parking,
          has_wifi: form.has_wifi,
          has_outdoor_seating: form.has_outdoor_seating,
          is_wheelchair_accessible: form.is_wheelchair_accessible,
          owner_first_name: form.owner_first_name.trim(),
          owner_last_name: form.owner_last_name.trim(),
          password: form.password,
          role: 'restaurant'
        }

        // Pour l'instant, simuler une inscription r√©ussie
        console.log('Donn√©es du restaurant:', restaurantData)
        
        successMessage.value = 'Restaurant inscrit avec succ√®s ! Redirection...'
        
        // Stocker les donn√©es du restaurant
        localStorage.setItem('restaurantData', JSON.stringify(restaurantData))
        
        // Rediriger vers le dashboard restaurant apr√®s 2 secondes
        setTimeout(() => {
          router.push('/restaurant-dashboard')
        }, 2000)
        
      } catch (err) {
        console.error('Erreur d\'inscription restaurant:', err)
        errorMessage.value = 'Une erreur inattendue s\'est produite'
      } finally {
        loading.value = false
      }
    }

    return {
      form,
      errors,
      loading,
      errorMessage,
      successMessage,
      handleRegister
    }
  }
}
</script>

